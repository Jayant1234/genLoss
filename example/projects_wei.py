# add extrapolated weights into the parameters
def phase2_extrapolation(model, valloader, device, baseline_state, stored_directions, num_epochs=1, lr_aux=1e-2):

    aux_params = torch.nn.Parameter(torch.randn(10, device=device) * 0.01) #create a parameter object with 10 random scalars
    optimizer_aux = optim.Adam([aux_params], lr=lr_aux)# apply them to optimizer
    criterion = nn.CrossEntropyLoss()
    aux_history = []
    model.eval()

    for epoch in range(num_epochs):
        running_loss = 0.0
        total = 0
        correct = 0

        for batch_idx, (inputs, targets) in enumerate(valloader):
            inputs, targets = inputs.to(device), targets.to(device)

            optimizer_aux.zero_grad()
            # extrapolated parameter：θ_extrapolated = baseline_state + Σ a_i * d_i, Implements the Formula
            extrapolated_state = state_dict_linear_combination(baseline_state, stored_directions, aux_params)

            outputs = functional_call(model, extrapolated_state, (inputs,))
            loss = criterion(outputs, targets)
            loss.backward()
            optimizer_aux.step()

            running_loss += loss.item()
            _, predicted = outputs.max(1)
            total += targets.size(0)
            correct += predicted.eq(targets).sum().item()

        acc = 100. * correct / total
        print(f"[Extrapolation Epoch {epoch+1}] Loss: {running_loss/len(valloader):.4f} | Acc: {acc:.2f}%")
        aux_history.append(aux_params.detach().cpu().clone())

    return aux_params, aux_history

# -----------------------------
# Evaluation function on test set using extrapolated weights
# -----------------------------
def evaluate_extrapolated(model, testloader, device, baseline_state, stored_directions, aux_params):
    model.eval()
    total = 0
    correct = 0
    with torch.no_grad():
        extrapolated_state = state_dict_linear_combination(baseline_state, stored_directions, aux_params)
        for batch_idx, (inputs, targets) in enumerate(testloader):
            inputs, targets = inputs.to(device), targets.to(device)
            outputs = functional_call(model, extrapolated_state, (inputs,))
            _, predicted = outputs.max(1)
            total += targets.size(0)
            correct += predicted.eq(targets).sum().item()
    acc = 100. * correct / total
    print(f"Final Extrapolated Model Accuracy on Test Set: {acc:.2f}%")
    return acc


def main():
    trainloader, valloader, testloader = get_dataloaders(batch_size=128, val_split=0.1)
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")

    model = get_model().to(device)

    print("=== Phase 1: Baseline Training (Epochs 1-10) ===")
    baseline_state, stored_directions = phase1_train(model, trainloader, device, num_epochs=10, lr=0.1, momentum=0.9)

    print("\n=== Phase 2: Extrapolation Training (Epoch 11) using Validation Set ===")
    aux_params, aux_history = phase2_extrapolation(model, valloader, device, baseline_state, stored_directions, num_epochs=1, lr_aux=1e-2)

    print("\n=== Final Evaluation on Test Set ===")
    evaluate_extrapolated(model, testloader, device, baseline_state, stored_directions, aux_params)


if __name__ == "__main__":
    main()

